package com.kone.Elevators;
/*
 * console class contains the method to print the message or error to the console.
 */
public class Console {

/*
 * writeInfo method is used to print output to the screen
 */
	public void writeInfo(String message) {

		
		
		System.out.println(message);
	}
/*
 * 	writeError method is used to print error to the screen
 */
	public void writeError(String message){
		System.out.println(message);
	}

}


